          <p> Steel racking is a cheaper option than aluminium shelves, and also are durable as well.Steel racking are heavier
            and more flexible than aluminium racking systems.Flexibility is an added advantage when building in a van. If you
            are storing heavy materials and equipments, then steel racking is an excellent choice.

          </p>
          <h3> Hybrid shelves </h3>
          <p> These shelves combine the strength of steel and the light weight property of the aluminium.Therefore,
            making it one of the most popular racking system.Additionally they are very flexible as well.

          </p>
          <h3> Wood Racking </h3>
          <p> The main feature of wood racking is they can be customised according to your preferences,
            so they are the racking system to choose when it comes to tailored van build.But, it cannot
            be used for heavy materials and equipment.

          </p>

